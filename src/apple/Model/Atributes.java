package apple.Model;

public class Atributes {
    private String nome;
    private String marca;
    private String sistemaOperacional;
    private int id;

    public Atributes(String marca, String nome, String sistemaOperacional, int id) {
        this.marca = marca;
        this.nome = nome;
        this.sistemaOperacional = sistemaOperacional;
        this.id = id;
    }

}

